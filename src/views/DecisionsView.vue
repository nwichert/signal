// Simple markdown parser for bold, italic, and line breaks
function parseMarkdown(text: string): string {
  if (!text) return ''
  return text
    // Bold: **text** or __text__
    .replace(/\*\*(.+?)\*\*/g, '<strong class="font-semibold text-gray-900">$1</strong>')
    .replace(/__(.+?)__/g, '<strong class="font-semibold text-gray-900">$1</strong>')
    // Italic: *text* or _text_
    .replace(/\*(.+?)\*/g, '<em>$1</em>')
    .replace(/_(.+?)_/g, '<em>$1</em>')
    // Horizontal rule: ---
    .replace(/^---$/gm, '<hr class="my-3 border-gray-200">')
    // Line breaks
    .replace(/\n/g, '<br>')
}

// Get plain text preview (strip markdown)
function getPlainTextPreview(text: string, maxLength: number = 150): string {
  if (!text) return ''
  const plain = text
    .replace(/\*\*(.+?)\*\*/g, '$1')
    .replace(/__(.+?)__/g, '$1')
    .replace(/\*(.+?)\*/g, '$1')
    .replace(/_(.+?)_/g, '$1')
    .replace(/^---$/gm, '')
    .replace(/\n+/g, ' ')
    .trim()
  return plain.length > maxLength ? plain.substring(0, maxLength) + '...' : plain
}

