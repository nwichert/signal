}

function toggleHypothesisSelection(hypothesisId: string) {
  const idx = selectedHypothesisIds.value.indexOf(hypothesisId)
  if (idx >= 0) {
    selectedHypothesisIds.value.splice(idx, 1)
  } else {
    selectedHypothesisIds.value.push(hypothesisId)
  }
}

